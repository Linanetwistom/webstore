public class FileUploadIoException extends FileUploadException{

    public FileUploadIoException() {
    }

    public FileUploadIoException(String arg0) {
        super(arg0);
    }

    public FileUploadIoException(Throwable arg0) {
        super(arg0);
    }

    public FileUploadIoException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public FileUploadIoException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }

}
